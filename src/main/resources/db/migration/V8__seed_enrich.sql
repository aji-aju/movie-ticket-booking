--
-- IDs: every table is GENERATED BY DEFAULT AS IDENTITY. We omit ids everywhere and use
-- RETURNING ... INTO (so the identity sequence always advances -> no runtime PK clashes).
--
-- Pricing model (matches show 1): show_seat.price = base_price for REGULAR, base_price+100
-- for PREMIUM. The weekend x1.25 surcharge is applied at BOOKING time from the seat's
