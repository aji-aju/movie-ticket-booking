-- Materialize show_seats for every show that doesn't have them yet (i.e. the new ones;
-- show 1 already has its 10). Set-based and id-agnostic: each show gets one bookable seat
-- per seat of its screen, priced by tier off the show's base_price.
INSERT INTO show_seat (show_id, seat_id, status, price)
SELECT sh.id, s.id, 'AVAILABLE',
       CASE WHEN s.tier = 'PREMIUM' THEN sh.base_price + 100.00 ELSE sh.base_price END
FROM shows sh
JOIN seat s ON s.screen_id = sh.screen_id
WHERE NOT EXISTS (
    SELECT 1 FROM show_seat ss WHERE ss.show_id = sh.id AND ss.seat_id = s.id
);

